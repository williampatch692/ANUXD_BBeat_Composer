s = x => 2 ** ((x + .15) / 12),
d = 12168,
g = 15 & t >> 15,
v = 4096,
// Melody
a = t => (
	// Sequence melody
	p = t * s([
		2, 4, 5, 9, 2, 4, 5, 9, 2, 4, 5, 9, 12, 9, 7, 5, 0, 2, 4, 7, 0, 2,
		4, 7, 0, 2, 4, 7, 0, 2, 4, 0, -2, 0, 2, 5, -2, 0, 2, 5, -2, 0, 2, 5,
		-2, 0, 2, -2, -3, -1, 1, 4, -3, -1, 1, 4, -3, -1, 1, 7, -3, -1, 1, 9
	][(t >> 13) % 64]),
	(p * 2 & 128) / 1.5
) * (1 - t % 8192 / 12E3) * (t / v > 1152 ? 0 : 1),
// Sequence bass
b = t * s([2, 2, 2, 5, 0, 0, 0, 0, -2, -2, -2, -2, -3, -3, -3, 1][g]) / 4,
// Sequence motif
m = [2, 2, 2, 0, 0, 0, 0, 0, -2, -2, -2, -2, -3, -3, -3, 1][g],
m2 = [5, 5, 5, 5, 4, 4, 4, 4, 2, 2, 2, 2, 1, 1, 1, 4][g],
m3 = [9, 9, 9, 9, 7, 7, 7, 7, 5, 5, 5, 5, 4, 4, 4, 7][g],
// Mute extends
mt = t / v > 1280 ? 0 : 1,
mt2 = t / v > 1024 ? 0 : 1,
mt3 = t / v > 1088 ? 0 : 1,
// Stereo delay effect was helped by Kouzerumatsu
L = a(t) + a(t - d) / 2 + a(t - d * 3) / 4 + a(t - d * 5) / 8,
R = a(t) + a(t - d * 2) / 2 + a(t - d * 4) / 4 + a(t - d * 6) / 8,
// Bass
k = ((b % 256) + (b % 254)) / 6.5,
// Decay rate on motif
x = '1112111111111112'[g],
// Motif
mtf = (s([m, m2, m3][(t >> 10) % 3]) * t & 32) / 1.5 * (1 - t * x % 65536 / 65E3 / x),
// Snare
ns = random() * [10, 0, 10, 0, 16, 0, 10, 0][7 & t >> 12] * 1.2,
// Drum
dr = (4E3 / (t * '1 121121 1 21124'[15 & t >> 13] % 8192) ** .1 & 32) * 1.4,
// Crash
cr = (t * sin(t >> 3) & -t >> 8 & 255) / 3.75 * (t & 32768) / 32E3,
// Extends
h = (t / v > 128 ? k : 0) * mt +
	(t / v > 256 ? mtf : 0) * mt2 +
	(t / v > 384 ? ns + dr : 0) * mt3 +
	(t / v > 512 ? cr : 0) * mt3,
// Output
[L / 1.9 + h, R / 1.9 + h];
